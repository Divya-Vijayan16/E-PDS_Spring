package com.dto;

public class ShopstockDetails {
	String product;
	String availQty;
	String allotedQty;
	public String getProduct() {
		return product;
	}
	public void setProduct(String product) {
		this.product = product;
	}
	public String getAvailQty() {
		return availQty;
	}
	public void setAvailQty(String availQty) {
		this.availQty = availQty;
	}
	public String getAllotedQty() {
		return allotedQty;
	}
	public void setAllotedQty(String allotedQty) {
		this.allotedQty = allotedQty;
	}
	

}
