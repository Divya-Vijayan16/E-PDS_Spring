package com.dto;

public class AllotProduct {
	String productNo;
	String shopNo;
	String riceQty;
	String wheatQty;
	String sugarQty;
	String city;
	String area;
	public String getProductNo() {
		return productNo;
	}
	public void setProductNo(String productNo) {
		this.productNo = productNo;
	}
	public String getShopNo() {
		return shopNo;
	}
	public void setShopNo(String shopNo) {
		this.shopNo = shopNo;
	}
	public String getRiceQty() {
		return riceQty;
	}
	public void setRiceQty(String riceQty) {
		this.riceQty = riceQty;
	}
	public String getWheatQty() {
		return wheatQty;
	}
	public void setWheatQty(String wheatQty) {
		this.wheatQty = wheatQty;
	}
	public String getSugarQty() {
		return sugarQty;
	}
	public void setSugarQty(String sugarQty) {
		this.sugarQty = sugarQty;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}

	

}